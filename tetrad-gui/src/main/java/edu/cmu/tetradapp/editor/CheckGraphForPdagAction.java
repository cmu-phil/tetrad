package edu.cmu.tetradapp.editor;

import edu.cmu.tetrad.graph.Graph;
import edu.cmu.tetradapp.util.GraphUtils;
import edu.cmu.tetradapp.workbench.GraphWorkbench;

import javax.swing.*;
import java.awt.event.ActionEvent;

/**
 * CheckGraphForPdagAction is an action class that checks if a given graph is a legal PDAG (Maximal Partially Directed
 * Acyclic Graph) and displays a message to indicate the result.
 */
public class CheckGraphForPdagAction extends AbstractAction {

    /**
     * The desktop containing the target session editor.
     */
    private final GraphWorkbench workbench;

    /**
     * Highlights all latent variables in the given display graph.
     *
     * @param workbench the given workbench.
     */
    public CheckGraphForPdagAction(GraphWorkbench workbench) {
        super("Check to see if Graph is a PDAG");

        if (workbench == null) {
            throw new NullPointerException("Desktop must not be null.");
        }

        this.workbench = workbench;
    }

    /**
     * This method is used to perform an action when an event is triggered, specifically when the user clicks on a
     * button or menu item associated with it. It checks if a graph is a legal PDAG (Maximal Partially Directed
     * Acyclic Graph).
     *
     * @param e The ActionEvent object that represents the event generated by the user action.
     */
    public void actionPerformed(ActionEvent e) {
        Graph graph = workbench.getGraph();

        if (graph == null) {
            JOptionPane.showMessageDialog(GraphUtils.getContainingScrollPane(workbench), "No graph to check for PDAGness.");
            return;
        }

        if (graph.paths().isLegalPdag()) {
            JOptionPane.showMessageDialog(GraphUtils.getContainingScrollPane(workbench), "Graph is a legal PDAG.");
        } else {
            JOptionPane.showMessageDialog(GraphUtils.getContainingScrollPane(workbench), "Graph is not a legal PDAG.");
        }
    }
}




